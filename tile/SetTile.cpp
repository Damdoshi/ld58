#include		"Tile.hpp"

void			Tile::SetTile(unsigned int	mat,
				      unsigned int	hei)
{
  height = hei;
  type = SAND;
  if (mat != WATER && mat != SAND && mat != MINERAL && mat != ROCK)
    return ;
  type = (Type)mat;
}

